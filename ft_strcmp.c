#include "libft.h"
#include <stdio.h>

int		ft_strcmp(char const *s1, char const *s2)
{
	size_t		i;

	i = 0;
	while (s1[i] == s2[i])
	{
		if (s1[i] == '\0')
			break ;
		i++;
	}
	return (s1[i] - s2[i]);
}
